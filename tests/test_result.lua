BInt.register_test("result/ok_creation", function(t)
	local r = BInt.Ok({ value = 42 })
	t:assert_true(r.success, "Ok should be successful")
	t:assert_eq(r.value, 42, "data should be preserved")
end)

BInt.register_test("result/fail_creation", function(t)
	local r = BInt.Fail("something broke", { code = 404 })
	t:assert_false(r.success, "Fail should not be successful")
	t:assert_eq(r.reason, "something broke")
	t:assert_eq(r.code, 404, "extra data should be preserved")
end)

BInt.register_test("result/ok_assert_passes", function(t)
	local r = BInt.Ok()
	r:assert("this should not fail")
	t:log("Ok:assert() passed as expected")
end)

BInt.register_test("result/fail_assert_fails", function(t)
	local r = BInt.Fail("broken")
	r:assert("expected failure")
	t:log("THIS SHOULD NOT PRINT")
end)

BInt.register_test("result/ok_assert_fail_fails", function(t)
	local r = BInt.Ok()
	r:assert_fail("expected failure")
	t:log("THIS SHOULD NOT PRINT")
end)

BInt.register_test("result/fail_assert_fail_passes", function(t)
	local r = BInt.Fail("broken")
	r:assert_fail("this should not fail")
	t:log("Fail:assert_fail() passed as expected")
end)

BInt.register_test("result/chaining", function(t)
	local r = BInt.Ok({ value = 10 })
	local returned = r:assert()
	t:assert_eq(returned, r, "assert should return self for chaining")
end)

BInt.register_test("result/empty_ok", function(t)
	local r = BInt.Ok()
	t:assert_true(r.success)
	r:assert()
end)

BInt.register_test("result/empty_fail", function(t)
	local r = BInt.Fail("reason")
	t:assert_false(r.success)
	t:assert_eq(r.reason, "reason")
	r:assert_fail()
end)
